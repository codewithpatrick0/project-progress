package Fundaments.Operadores;

public class OperadoresComparacion {
    public static void main(String[] args) {
        int x = 5;
        int y = 2;

        //salida
        System.out.println("Los datos son: " + x + " y " + y + " respectivamente");
        System.out.println("x == y: " + (x == y));
        System.out.println("x != y:" + (x != y));
        System.out.println("x < y: " + (x < y));
        System.out.println("x > y: " + (x > y));
        System.out.println("x <= y: " + (x <= y));
        System.out.println("x >= y: " + (x >=y));
    }
}
